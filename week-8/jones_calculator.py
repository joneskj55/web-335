# add function
def add(add1, add2):
    return add1 + add2

# subtract function
def subtract(sub1, sub2):
    return sub1 - sub2

# divide function
def divide(div1, div2):
    return div1 / div2

# output answers
print(add(1, 2))
print(subtract(4, 1))
print(divide(8, 2))
